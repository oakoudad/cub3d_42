#include "../cub3d.h"

void	set_direction(t_elm_map *map, char c)
{
	if (c == 'N')
		map->dir = 180;
	if (c == 'E')
		map->dir = 270;
	if (c == 'S')
		map->dir = 0;
	if (c == 'W')
		map->dir = 90;
}
