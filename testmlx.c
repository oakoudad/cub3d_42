typedef struct player
{
	int x;
	int y;
}p;
typedef struct	s_data {
	void	*img;
	char	*addr;
	int		bits_per_pixel;
	int		line_length;
	int		endian;
	p		pl;
	char *x;
}				t_data;

void	my_mlx_pixel_put(t_data *data, int x, int y, int color)
{
	char	*dst;

	dst = data->addr + (y * data->line_length + x * (data->bits_per_pixel / 8));
	*(unsigned int*)dst = color;
}
void draw(t_data *img)
{
	int i = 0;
	int j = 0;
	while (i < 620)
	{
		j = 0;
		while (j < 800)
		{
			if (i/20%2 || j/20%2)
				my_mlx_pixel_put(img, i, j, 0x00FF0000);
			if(i/20%2 || j/20%2)
				my_mlx_pixel_put(img, i, j,0xFFFFFF);
			else
			{
				img->pl.x = i/20;
				img->pl.y = j/20;
				my_mlx_pixel_put(img, i, j,0xFF00FF);
			}
			j++;
		}
		i++;
	}
}
int move(int key, t_data *img)
{
	
	printf("***> %d \n", key);
	if (key == 13)
	{
		img->pl.x+= 10;
		img->pl.y+= 10;
	}
	draw(img);
	return(key);
}

